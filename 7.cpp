//To calculate the simple interest
#include <iostream>
using namespace std;
int main(){
    float Principal = 1000;
    float Rate = 2;
    float Time = 3;
    float simpleinterest = Principal*Rate*Time;
    cout<<"Principal="<<Principal<<endl;
    cout<<"Rate="<<Rate<<endl;
    cout<<"Time="<<Time<<endl;
    cout<<"Simpleinterest="<<simpleinterest<<endl;
    return 0;
}
